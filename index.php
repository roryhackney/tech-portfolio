How I Developed A Database
Posted on August 24, 2021	Posted in tags...

I recently developed a database for my small art business, and wanted to show how I did it. There was a lot of design work,
documentation, and analysis that went into it… not to mention all the SQL code! I learned a lot, and had a lot of fun.
Here’s the highlights!

Technology
I designed my database diagram (ERD) using Figma. I used Google Cloud to host my MariaDB database, which I managed in phpMyAdmin using SQL.

Resources
I’d like to thank Stack Overflow, W3Schools, and Database Design For Mere Mortals by Michael Hernandez, without which this project
would have been a whole lot harder.

Background
Rory’s Stories is a small art business I run that promotes my art via social media and other sites (such as Patreon), as well as
selling both digital and physical art products and commissions.

Unfortunately, my business transactions were scattered over multiple sites (rorystories.com, Instagram, Patreon, email…) and
I couldn’t find info on clients, products, or other items easily.

I needed a database.

[Digital illustration of a forest (Rory’s Stories)]

The Plan

In order to design a database, several complex tasks needed to be completed. By making a plan, I was able to ensure all aspects
of the database design were accounted for.

Step 1 – Define the goal. What are the business needs and rules?

Step 2 – Design the database. How will the structure fulfill the goal?

Step 3 – Plan user access. Who will use the data, how, and what reports will they need?

Step 4 – Create the database. Tables, data, users, and views.

Step 5 – Test the database. User access, objectives, export ERD and backup database.

Skip to results

The Goal

The goal of my database design project was to make my business’s data easy to store and retrieve from a single place.
To define what data needed to be tracked, I reviewed my business plan, previous client work and communications, and web content.
I then came up with this list:

Mission Objectives
    Track products (both physical and digital)
    Track stock levels (physical products only)

    Track portfolio pieces
    Track customers (including subscribers)
    Track commissions

    Track expenses
    Track incomes
    Track orders
(and more…)

Business Rules

I also made a list of business considerations that would affect the design:

    If all items in an order are digital, shipping should not be charged.
    Patreon members should receive a 15% discount on digital items and commissions.
    No more than 15 commission requests can be in process at the same time.
    Products and portfolio pieces should be filterable and sortable by project, category, format, tags, year made, and price.
(and much more…)

The goal of the database was now clearly defined.

Back to the plan

The Design
Once I understood the goal, I was able to start designing the database. I started by listing all the fields I might use, then
grouped them by subject to create tables.

Fields
    Customer ID, Customer First Name, Customer Last Name, Customer Preferred Name, Customer Street Address, Customer City, Customer State, Customer Zip, Customer Email, Customer Phone, Customer Patreon Level, Customer Newsletter Subscription
    Portfolio Piece ID, Portfolio Piece Name, Portfolio Piece Year Made, Portfolio Piece Blurb, Portfolio Piece Description
(and much, much more…)

Tables
    Projects
    Products
    Product Images
    Portfolio Pieces

    Orders
    Inventory
    Commissions
    Customers

    Incomes
    Expenses
    Sales

Research
I also researched other database designs to see how they structured their data, and used what I learned to create my initial
database diagram.

After viewing moqups.com’s ecommerce ERD, I added Product Number in a new Inventory table to track stock of physical products.
I also added weight, length, width, and height. I then added Quantity to the Order Details table, and Shipping Address to Orders.

After [more research], I added unit cost to Products, Images table, and States lookup table.

ERD (Initial Database Diagram)

Once I had my lists of tables and fields, I used them to build my ERD.

[Crow’s Foot ERD for Rory’s Stories – version 1]

Normalization
I then ran my ERD through normalization, ensuring the design avoided common database design problems.
ERD passes 1st Normal Form – each table represents a single subject, fields describe or relate to table subject, each cell has
only one value (no multivalued fields), each column has a unique name except primary and foreign keys, each record can be uniquely
identified (PK), order of fields and records don’t matter.
(and more…)
I also had my design reviewed for issues, and checked data integrity again.

Peer Review
Removed productID from Images table (already in ProductImages linking table)

Data Integrity
    Removed duplicate field name formatType from ProductOrders (redundant due to formatID in Products). Added primary keys to
    linking tables PortfolioTags and ProductImages.
    Split multipart field imageSize in Images table up into imageHeight and imageWidth.
    Relationship between Customers and Orders not indicated on the diagram. Changed to one (Customers) to many (Orders) relationship.
    No other duplicate field names, calculated or concatenated fields, multipart or multivalued fields, or duplicate records, and all
    tables have a primary key field.
    Fields represent a distinct characteristic of tables, cannot be split apart, not calculated or concatenated, and are unique within
    the database.
    Relationships are correctly defined, and use primary keys, foreign keys, or linking tables to establish relationships as appropriate.

ERD (Final Database Diagram)
[Crow’s Foot ERD for Rory’s Stories – version 2]
The database structure was now carefully designed.
Back to the plan

The Users
The last step before I could create my database was to think about who would use it, what data they should be able to access, and
what reports and views they would need.

Stakeholders

Because my business is operated by one person, the users are largely task-specific or external.
    Business owner
    Database admin

    Product manager
    Customer service

    Customers
    Accountant

Security Diagram
I then planned how much access each user type should have to each table in the database. Only the database admin would have full access.
[Security diagram for Rory's Stories database design project]

Views
After that, I designed views for the database, which would quickly generate reports for users.
Orders View displays each order’s products, customer, and order info.
Tables: Orders, Customers, ProductOrders, Products, Formats, PortfolioPieces, Categories
Fields: orderID, orderDate, orderFulfillDate, customerFirstName, customerLastName, customerPreferredName, customerEmail, customer Phone,
categoryName, portfolioPieceName, productOrderQuantity, formatName
(and more…)
User profiles, security, and reports had now been planned.

Back to the plan

The Implementation
I started building the database by composing SQL code to create each table, starting with independent tables and then moving through the
tables that relied on them.
[Create Tables SQL Code – Scroll Video]

Design Changes
As I implemented the database, I improved some areas of the design, making sure to update the ERD and documentation accordingly.
    Added PhysicalInventory table to track physical items, replacing productNumber from Products table. Added one to many relationship
    to the ERD.
    Renamed formatType to formatIsDigital (boolean) to improve clarity. Renamed measurement fields to include units, as in
    productHeightInches.
    Added password field to Customers table (which would of course be securely salted and encrypted) to allow account management and login.
    Updated Security Diagram to include States and PhysicalInventory table.

As I entered data into the tables, using actual business data wherever possible, I realized some of my data types were off. For example,
I originally used integers for height (inches), but some products were in between. Rather than enter inexact data, I changed the data type
to decimal(6,2) which allowed me to precisely enter the correct number.

Data Entry
Once all the design issues had been resolved, I was able to enter data into each table using SQL.
[Insert Data SQL Code – Scroll Video]

Views and Users
After that, I created the views I designed, and saved them for easy access. I also created the users, and made sure to set their access
privileges carefully. Implementation was now complete.

Back to the plan

The Tests

Once the database was complete, I made sure it fulfilled the goals of the project by testing each mission objective and business rule
against the database using SQL and views.
    The database should track commissions and freelance work.
SELECT commissionDate, commissionDescription, commissionFulfillDate, commissionPaidDate, commissionPrice,  commissionSource,
commissionType, Concat(customerFirstName, ‘ ‘, customerLastName) AS customerName, customerPreferredName
FROM Commissions INNER JOIN Customers ON Commissions.customerID =  Customers.customerID
