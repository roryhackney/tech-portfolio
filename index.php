How I Designed A Massage Therapist’s Website
Posted on August 24, 2021	Posted in tags...

[hero img of finished site]

Back in April 2021, I designed a website for a local massage therapist.

Background
The client was a local, tech-phobic massage therapist in Seattle kept very busy by the demands of her business.
I therefore planned the site to screen clients, answer questions, and keep as much work as possible off her plate,
without adding to her tasks by requiring technical knowledge or content management.

My Process
I first conducted client interviews, which I used to develop documentation on business goals for the website.
These ranged from defining the audience and benefit to the business owner to the design choices, UI, and visuals
required to accomplish these goals.

Website Design Goals
Create web presence with accurate information to circumvent false information posted on other sites that may confuse clients.
Save time and energy by answering client questions, screening new clients, and providing information on policies and legal notices.

Audience
Current clients needing information, some potential clients, screening potential clients by providing availability info.
Age: 30-80 Gender: Any Occupation: Any, esp. ones that strain back or other areas Online frequency: little to mid Financial:
low to mid Insurance: often, but not always (sliding scale available) Situation: may have an injury, massage may be part of their
recovery plan

Example
Bob is a 60-year old man, close to retirement, who just strained his back at work. He goes to the website to find out if his insurance
will cover treatment, how to contact her, and if she has availability.
(and more…)

I then built information architecture and conducted competitive analyses to plan the content of the website, particularly around content
such as legal notices, allergens, and policies.

[Information architecture diagram for massage website]

After that, I created wireframes and a visual design study. I then obtained feedback from peers and the client and made a list of changes
to be made to the prototype.

Design Changes
    Reduced text size and increased white space and margins
    Added decorative images, including a spotlight image on home
    Desaturated colors, went with more subtle blue/yellow color scheme
    Added white text shadow to name on Home Page to improve readability
    Changed nav from boxes to a single bar
    Reduced number of colors, increased value contrast
    Changed alert from ‘New clients will not be accepted’ to ‘Not accepting new clients’
    Formatted about page text and other areas using colored divs
    Changed pricing page tabs to box navigation on horizontal bar
    Inserted real content based on competitive analyses and feedback
    Reduced boxiness of design, text
    Removed color from main content background, other than light text divs
    Ran colors through contrast checker, shifted link colors for readability
    Removed unnecessary content and shrank remaining content

